// TYPE CASTING 

import java.util.*;
public class TypeCasting {
    public static void main(String[] args){
        float a = 25.9f;
        int b = (int)a;
        System.out.println(b);

        char ch = 'a';
        int number = ch;
        System.out.println(number);

        float A = 27.9f;
        int B = (int)A;
        System.out.println(B);

        double C = 289.98;
        long D = (long)C;
        System.out.println(C);

    }
}
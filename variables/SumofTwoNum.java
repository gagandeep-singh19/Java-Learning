// Sum of a &  b 
import java.util.*;
public class SumofTwoNum {
    public static void main(String[]args){
        Scanner sc = new Scanner(System.in);
        int A = sc.nextInt();
        int B = sc.nextInt();
        int sum = A + B;
        
        System.out.println("sum of A & B :" +sum);
    }
}